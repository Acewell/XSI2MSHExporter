//
// GET MINIMUM AND MAXIMUM VECTOR
//
void __cdecl GetMinMax (
						ZeroVector3 &Min,
						ZeroVector3 &Max,
						const float *VertexArray,
						int StrideInBytes,
						int VertexCount)
{
	int i;
	int Stride;
	
	Stride = StrideInBytes>>2;
	_ASSERTE (Stride >= 3);
	Min = Max = ZeroVector3 (VertexArray[0], VertexArray[1], VertexArray[2]); 
	VertexArray += Stride;
	for (i = 1; i < VertexCount; i ++) {
		if (VertexArray[0] < Min.x) {
			Min.x = VertexArray[0]; 
		}
		if (VertexArray[1] < Min.y) {
			Min.y = VertexArray[1]; 
		}
		if (VertexArray[2] < Min.z) {
			Min.z = VertexArray[2]; 
		}
		if (VertexArray[0] > Max.x) {
			Max.x = VertexArray[0]; 
		}
		if (VertexArray[1] > Max.y) {
			Max.y = VertexArray[1]; 
		}
		if (VertexArray[2] > Max.z) {
			Max.z = VertexArray[2]; 
		}
		VertexArray += Stride;
	}
}



